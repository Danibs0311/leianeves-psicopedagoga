DROP POLICY IF EXISTS "Admins can select documents" ON public.patient_documents;
DROP POLICY IF EXISTS "Admins can insert documents" ON public.patient_documents;
DROP POLICY IF EXISTS "Admins can update documents" ON public.patient_documents;
DROP POLICY IF EXISTS "Admins can delete documents" ON public.patient_documents;

DROP POLICY IF EXISTS "Admins can select templates" ON public.document_templates;
DROP POLICY IF EXISTS "Admins can insert templates" ON public.document_templates;
DROP POLICY IF EXISTS "Admins can update templates" ON public.document_templates;
DROP POLICY IF EXISTS "Admins can delete templates" ON public.document_templates;

DROP POLICY IF EXISTS "Admins can select records" ON public.clinical_records;
DROP POLICY IF EXISTS "Admins can insert records" ON public.clinical_records;
DROP POLICY IF EXISTS "Admins can update records" ON public.clinical_records;
DROP POLICY IF EXISTS "Admins can delete records" ON public.clinical_records;

-- ==============================================================================
-- APPOINTMENTS (Agendamentos Públicos)
-- ==============================================================================

-- PERMISSÃO DE INSERÇÃO PÚBLICA (Permitir que form do site adicione)
CREATE POLICY "Enable insert for all users" ON public.appointments FOR INSERT TO public WITH CHECK (true);
-- PERMISSÃO DE LEITURA RESTRITA (Somente painel administrativo logado)
CREATE POLICY "Enable select for authenticated only" ON public.appointments FOR SELECT TO authenticated USING (true);
-- PERMISSÃO DE EDIÇÃO RESTRITA (Mudar status pra conluído etc, só admin logado)
CREATE POLICY "Enable update for authenticated only" ON public.appointments FOR UPDATE TO authenticated USING (true) WITH CHECK (true);

-- Garante que o PostgREST deixe anônimos inserirem
GRANT INSERT ON TABLE public.appointments TO anon;
GRANT USAGE, SELECT ON SEQUENCE appointments_id_seq TO anon, authenticated;

-- ==============================================================================
-- DEMAIS TABELAS (Gerenciamento Interno - Somente Usuários Logados)
-- ==============================================================================

-- Pacientes (Patients)
CREATE POLICY "Admins can select patients" ON public.patients FOR SELECT TO authenticated USING (true);
CREATE POLICY "Admins can insert patients" ON public.patients FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "Admins can update patients" ON public.patients FOR UPDATE TO authenticated USING (true);
CREATE POLICY "Admins can delete patients" ON public.patients FOR DELETE TO authenticated USING (true);

-- Documentos e anexos (Patient Documents)
CREATE POLICY "Admins can select documents" ON public.patient_documents FOR SELECT TO authenticated USING (true);
CREATE POLICY "Admins can insert documents" ON public.patient_documents FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "Admins can update documents" ON public.patient_documents FOR UPDATE TO authenticated USING (true);
CREATE POLICY "Admins can delete documents" ON public.patient_documents FOR DELETE TO authenticated USING (true);

-- Modelos de Relatórios/Laudos (Document Templates)
CREATE POLICY "Admins can select templates" ON public.document_templates FOR SELECT TO authenticated USING (true);
CREATE POLICY "Admins can insert templates" ON public.document_templates FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "Admins can update templates" ON public.document_templates FOR UPDATE TO authenticated USING (true);
CREATE POLICY "Admins can delete templates" ON public.document_templates FOR DELETE TO authenticated USING (true);

-- Registros Clínicos Efetivos (Clinical Records)
CREATE POLICY "Admins can select records" ON public.clinical_records FOR SELECT TO authenticated USING (true);
CREATE POLICY "Admins can insert records" ON public.clinical_records FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "Admins can update records" ON public.clinical_records FOR UPDATE TO authenticated USING (true);
CREATE POLICY "Admins can delete records" ON public.clinical_records FOR DELETE TO authenticated USING (true);


