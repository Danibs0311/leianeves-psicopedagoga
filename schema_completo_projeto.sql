-- ==============================================================================
-- 1. CRIAÇÃO DAS TABELAS (SCHEMAS)
-- ==============================================================================

-- Tabela: AGENDAMENTOS (Appointments)
-- Guarda os pedidos de consulta vindos do site principal (Fila de Espera/Agendamento).
CREATE TABLE IF NOT EXISTS public.appointments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    parent_name TEXT NOT NULL,         
    child_name TEXT NOT NULL,          
    child_age INTEGER,                 
    email TEXT NOT NULL,               
    phone TEXT NOT NULL,               
    concern TEXT,                      
    preferred_date DATE NOT NULL,      
    preferred_time TEXT NOT NULL,      
    status TEXT DEFAULT 'pending' CHECK (status IN ('pending', 'confirmed', 'cancelled', 'completed')),
    google_event_id TEXT,              
    notes TEXT,                         
    cpf TEXT                           -- Adicionado coluna de CPF
);

-- Tabela: DISPONIBILIDADE (Availability Slots - Opcional)
-- Caso queira gerenciar blocos de horário fixos ao invés de usar só o Calendário do Google.
CREATE TABLE IF NOT EXISTS public.availability_slots (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    day_of_week INTEGER,               
    start_time TIME NOT NULL,
    end_time TIME NOT NULL,
    is_active BOOLEAN DEFAULT TRUE
);

-- Tabela: PACIENTES (Patients)
-- Guarda o cadastro principal dos pacientes ativos da clínica.
CREATE TABLE IF NOT EXISTS public.patients (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    child_name TEXT NOT NULL,
    parent_name TEXT NOT NULL,
    phone TEXT,
    email TEXT,
    birth_date DATE,
    notes TEXT,
    status TEXT DEFAULT 'active', -- active, archived
    cpf TEXT UNIQUE               -- Adicionado coluna de CPF
);

-- Tabela: DOCUMENTOS DE PACIENTES (Patient Documents)
-- Anexos avulsos (Laudos, exames externos, imagens) enviados ou atrelados ao paciente.
CREATE TABLE IF NOT EXISTS public.patient_documents (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    patient_id BIGINT REFERENCES public.patients(id) ON DELETE CASCADE NOT NULL,
    title TEXT NOT NULL,
    description TEXT,
    file_url TEXT NOT NULL,
    file_type TEXT 
);

-- Tabela: MODELOS DE DOCUMENTOS (Document Templates)
-- Textos e templates HTML pré-prontos para laudos, informes, encaminhamentos, etc.
CREATE TABLE IF NOT EXISTS public.document_templates (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    title TEXT NOT NULL,
    content TEXT NOT NULL,
    category TEXT
);

-- Tabela: REGISTROS CLÍNICOS / DOCUMENTOS PREENCHIDOS (Clinical Records)
-- O histórico de evolução, sessões e relatórios efetivamente emitidos para o paciente.
CREATE TABLE IF NOT EXISTS public.clinical_records (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    patient_id BIGINT REFERENCES public.patients(id) ON DELETE CASCADE NOT NULL,
    template_id BIGINT REFERENCES public.document_templates(id),
    title TEXT NOT NULL,
    content TEXT NOT NULL,
    date DATE DEFAULT CURRENT_DATE
);


-- ==============================================================================
-- 2. CRIAÇÃO DOS ÍNDICES DE OTIMIZAÇÃO DE BUSCA
-- ==============================================================================
CREATE INDEX IF NOT EXISTS idx_appointments_email ON public.appointments(email);
CREATE INDEX IF NOT EXISTS idx_appointments_date ON public.appointments(preferred_date);
CREATE INDEX IF NOT EXISTS idx_patients_cpf ON public.patients(cpf);
CREATE INDEX IF NOT EXISTS idx_appointments_cpf ON public.appointments(cpf);


-- ==============================================================================
-- 3. AJUSTES DE PERMISSÃO / SEGURANÇA (ROW LEVEL SECURITY - RLS)
-- ==============================================================================

-- 3.1 Habilitar RLS em todas as tabelas
ALTER TABLE public.appointments ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.patients ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.patient_documents ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.document_templates ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.clinical_records ENABLE ROW LEVEL SECURITY;

